package com.mycompany.main9;

/**
 *
 * @author Ana Beatriz
 */
public class Endereco {
    
    protected String logradouro;
    protected String numero;
    protected String complemento;
    protected String CEP;
    protected String cidade;
    protected UnidadeFederativa uf;

    public Endereco(String logradouro, String numero, String complemento, String CEP, String cidade, UnidadeFederativa uf) {
        this.logradouro = logradouro;
        this.numero = numero;
        this.complemento = complemento;
        this.CEP = CEP;
        this.cidade = cidade;
        this.uf = uf;
    }

    public String getLogradouro() {
        return logradouro;
    }

    public void setLogradouro(String logradouro) {
        this.logradouro = logradouro;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getComplemento() {
        return complemento;
    }

    public void setComplemento(String complemento) {
        this.complemento = complemento;
    }

    public String getCEP() {
        return CEP;
    }

    public void setCEP(String CEP) {
        this.CEP = CEP;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public UnidadeFederativa getUf() {
        return uf;
    }

    public void setUf(UnidadeFederativa uf) {
        this.uf = uf;
    }

    @Override
    public String toString() {
        return "\nlogradouro:" + logradouro + "\nnumero:" + numero + "\ncomplemento:" + complemento + "\nCEP:" + CEP + "\ncidade:" + cidade + "\nuf:" + uf;
    }
    
    
    
}
